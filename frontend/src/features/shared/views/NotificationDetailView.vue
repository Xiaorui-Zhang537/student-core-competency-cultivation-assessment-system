<template>
  <div class="max-w-3xl mx-auto py-6">
    <PageHeader :title="t('shared.notifications.title') || t('layout.notifications') || '通知详情'" :subtitle="t('shared.notifications.subtitle') || ''" />
    <div v-if="loading" class="text-center text-gray-500 dark:text-gray-400">{{ t('notifications.loading') }}</div>
    <div v-else-if="!notification" class="text-center text-gray-500 dark:text-gray-400">
      {{ t('notifications.empty') }}
    </div>
    <div v-else class="rounded-xl p-6" v-glass="{ strength: 'regular', interactive: true }">
      <div class="flex items-start justify-between">
        <div class="flex items-center gap-2">
          <div class="h-9 w-9">
            <UserAvatar v-if="avatarUrl" :avatar="avatarUrl" :size="36">
              <span class="text-xs">{{ avatarNameInitial }}</span>
            </UserAvatar>
            <span v-else class="inline-flex items-center justify-center h-9 w-9 rounded-full bg-primary-100/70 dark:bg-primary-900/40 text-primary-700 dark:text-primary-300 ring-1 ring-primary-200/60 dark:ring-primary-800">
              <svg v-if="notification.type==='message'" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" class="w-5 h-5"><path d="M8 10h8M8 14h5"/><path fill-rule="evenodd" d="M4 5a2 2 0 012-2h12a2 2 0 012 2v10a2 2 0 01-2 2H9l-4 4V5z" clip-rule="evenodd"/></svg>
              <svg v-else-if="notification.type==='assignment'" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" class="w-5 h-5"><path d="M8 6h8v2H8zM8 10h8v2H8zM8 14h6v2H8z"/><path d="M5 4h14a1 1 0 011 1v14l-4-3H5a1 1 0 01-1-1V5a1 1 0 011-1z"/></svg>
              <svg v-else-if="notification.type==='grade'" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" class="w-5 h-5"><path d="M12 17l-5 3 1.9-5.9L4 9h6L12 3l2 6h6l-4.9 5.1L17 20z"/></svg>
              <svg v-else-if="notification.type==='course'" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" class="w-5 h-5"><path d="M4 6h16v2H4zM4 10h16v2H4zM4 14h10v2H4z"/></svg>
              <svg v-else xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" class="w-5 h-5"><path d="M12 2a9 9 0 00-9 9h3l-4 4-4-4h3a12 12 0 1124 0h-3l4 4 4-4h-3a9 9 0 00-9-9z" /></svg>
            </span>
          </div>
          <div>
            <h2 class="text-xl font-semibold text-gray-900 dark:text-white">{{ notification.title }}</h2>
            <p class="text-xs text-gray-500 dark:text-gray-400 mt-1">{{ formatTime(notification.createdAt) }}</p>
          </div>
        </div>
        <span class="text-xs px-2 py-1 rounded bg-primary-100/70 text-primary-700 dark:bg-primary-900/40 dark:text-primary-300 capitalize shadow-sm">{{ notification.type }}</span>
      </div>

      <div class="mt-4 p-4 rounded-lg bg-white/60 dark:bg-slate-800/50 text-gray-700 dark:text-gray-200 whitespace-pre-line">
        {{ notification.content }}
      </div>

      <div class="mt-6 flex flex-wrap gap-3">
        <Button v-if="!notification.isRead" variant="primary" size="sm" @click="markRead">
          <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" class="w-4 h-4"><path d="M9 12l2 2 4-4" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"/></svg>
          {{ t('notifications.actions.markRead') }}
        </Button>
        <Button variant="success" size="sm" @click="goRelated">
          <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor" class="w-4 h-4"><path stroke-linecap="round" stroke-linejoin="round" d="M13 7l5 5-5 5M6 12h12" /></svg>
          {{ t('notifications.actions.goRelated') }}
        </Button>
        <Button variant="outline" size="sm" @click="goCenter">
          <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor" class="w-4 h-4"><path stroke-linecap="round" stroke-linejoin="round" d="M15 19l-7-7 7-7" /></svg>
          {{ t('notifications.actions.backToCenter') }}
        </Button>
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
import { onMounted, computed } from 'vue'
import { useRoute, useRouter } from 'vue-router'
import { useNotificationsStore } from '@/stores/notifications'
import { useChatStore } from '@/stores/chat'
import { storeToRefs } from 'pinia'
// @ts-ignore
import { useI18n } from 'vue-i18n'
import UserAvatar from '@/components/ui/UserAvatar.vue'
import Button from '@/components/ui/Button.vue'
import PageHeader from '@/components/ui/PageHeader.vue'
import { submissionApi } from '@/api/submission.api'
import { gradeApi } from '@/api/grade.api'

const route = useRoute()
const router = useRouter()
const { t } = useI18n()
const notificationsStore = useNotificationsStore()
const { currentNotification: notification, loading } = storeToRefs(notificationsStore)
const chatStore = useChatStore()

const id = route.params.id as string
const isTeacher = computed(() => String(route.path || '').startsWith('/teacher'))
const base = computed(() => (isTeacher.value ? '/teacher' : '/student'))

onMounted(async () => {
  await notificationsStore.fetchNotificationDetail(id)
})

const markRead = async () => { await notificationsStore.markAsRead(id) }
const goCenter = () => { router.push(`${base.value}/notifications`) }

const goRelated = async () => {
  const n: any = notification.value
  if (!n) return
  const rt = String(n.relatedType || '').toLowerCase()
  const t = String(n.type || '').toLowerCase()

  // 消息类型：按 type 判定（忽略 relatedType 可能为 course 的情况）
  if (t === 'message') {
    const data: any = parseMeta()
    const myId = String(localStorage.getItem('userId') || '')
    // 优先使用 senderId/recipientId 判定对端
    const senderId = n.senderId != null ? String(n.senderId) : (data?.senderId != null ? String(data.senderId) : '')
    const recipientId = n.recipientId != null ? String(n.recipientId) : (data?.recipientId != null ? String(data.recipientId) : '')
    const peer = senderId && senderId !== myId ? senderId : (recipientId && recipientId !== myId ? recipientId : senderId || recipientId || '')
    const peerName = data?.senderName || n.senderName || n.title || ''
    const cid = String(n.relatedId || '') || undefined
    if (peer) {
      // 抑制下一次最近列表预览更新
      try { const mod = await import('@/stores/chat'); (mod as any).useChatStore().suppressNextPreviewUpdateOnce() } catch {}
      chatStore.openChat(peer, peerName, cid as any)
      return
    }
    return goCenter()
  }

  async function resolveRelatedAssignmentId(n: any): Promise<string> {
    try {
      const meta: any = parseMeta()
      const directAid = String(meta?.assignmentId || '')
      if (directAid) return directAid
      const kind = String(n.relatedType || '').toLowerCase()
      const ntype = String(n.type || '').toLowerCase()
      // 若为成绩通知：后端规范 relatedType=assignment，relatedId=assignmentId，直接返回，避免误将 assignmentId 当作 gradeId 调用接口
      if ((ntype === 'grade' || ntype === 'grade_posted') && kind === 'assignment' && n.relatedId) {
        return String(n.relatedId)
      }
      // 仅当明确拿到 gradeId 时，才用其反查 assignmentId（避免无意义的 400）
      const maybeGradeId = String((ntype === 'grade' || ntype === 'grade_posted') ? (meta?.gradeId || meta?.id || '') : '')
      if (maybeGradeId) {
        try {
          const gres: any = await gradeApi.getGradeById(maybeGradeId)
          const g = (gres && gres.data !== undefined) ? gres.data : gres
          const gaid = String((g as any)?.assignmentId || (g as any)?.assignment_id || '')
          if (gaid) return gaid
        } catch {}
      }
      const maybeSubmissionId = String(meta?.submissionId || (kind === 'submission' ? n.relatedId : ''))
      if (maybeSubmissionId) {
        const aid = await resolveAssignmentIdFromSubmission(maybeSubmissionId)
        if (aid) return aid
      }
      if (kind === 'assignment' && n.relatedId) return String(n.relatedId)
      const fallbackAid = String(n.relatedId || meta?.id || '')
      return fallbackAid
    } catch { return '' }
  }

  const to = async () => {
    switch (rt || t) {
      case 'assignment':
      case 'assignment_deadline':
        try {
          const aid = await resolveRelatedAssignmentId(n)
          if (!aid) return ''
          return isTeacher.value ? `${base.value}/assignments/${aid}/submissions` : `${base.value}/assignments/${aid}/submit`
        } catch { return '' }
      case 'submission': {
        try {
          const meta: any = parseMeta()
          const sid = String(n.relatedId || meta?.submissionId || meta?.id || '')
          if (!sid) return ''
          const aid = await resolveAssignmentIdFromSubmission(sid)
          if (!aid) return ''
          return isTeacher.value ? `${base.value}/assignments/${aid}/submissions` : `${base.value}/assignments/${aid}/submit`
        } catch { return '' }
      }
      case 'course':
      case 'course_update':
        return `${base.value}/courses/${n.relatedId}`
      case 'grade':
      case 'grade_posted': {
        if (!isTeacher.value) {
          const aid = await resolveRelatedAssignmentId(n)
          if (aid) return `${base.value}/assignments/${aid}/submit`
        }
        // 教师端：优先跳到该作业的提交列表
        {
          const aid = await resolveRelatedAssignmentId(n)
          if (aid) return `${base.value}/assignments/${aid}/submissions`
        }
        return `${base.value}/assignments`
      }
      case 'community_post':
        return `${base.value}/community/post/${n.relatedId}`
      default:
        return ''
    }
  }

  const target = await to()
  if (target) return router.push(target)
  return goCenter()
}

const formatTime = (timestamp: string) => new Date(timestamp).toLocaleString()

const parseMeta = () => {
  try {
    const raw: any = (notification.value as any)?.data
    if (!raw) return {}
    if (typeof raw === 'string') return JSON.parse(raw)
    if (typeof raw === 'object') return raw
  } catch {}
  return {}
}

async function resolveAssignmentIdFromSubmission(submissionId: string): Promise<string> {
  try {
    const res: any = await submissionApi.getSubmissionById(submissionId)
    const s = (res && res.data !== undefined) ? res.data : res
    const aid = String((s as any)?.assignmentId || (s as any)?.assignment_id || '')
    return aid || ''
  } catch {
    return ''
  }
}
const avatarUrl = computed(() => {
  const m: any = parseMeta()
  return m.senderAvatar || m.avatar || m.userAvatar || m.studentAvatar || m.teacherAvatar || ''
})
const avatarNameInitial = computed(() => {
  const m: any = parseMeta()
  const name = m.senderName || m.name || m.studentName || m.teacherName || ''
  return String(name || 'U').charAt(0).toUpperCase()
})
</script>


